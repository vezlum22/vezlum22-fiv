// Initialises Timer0 to generate TIMER0_COMPA IRQs ervery 125us
void timer0CTCInit()
{
    // Mode of Operation: CTC
    TCCR0A = (1 << WGM01);

    // Clock Select Bit set to: clk/8
    // fz/8 = 2 Mhz -> 500 ns per clock
    TCCR0B = (1 << CS01);
    
    // Timer0 Period: 125 us
    // 125 us / 500 ns = 250
    OCR0A = 249;    // 250 -1

    // Enable Interrupt: TIMER0_COMPA
    TIMSK0 = (1 << OCIE0A);
}
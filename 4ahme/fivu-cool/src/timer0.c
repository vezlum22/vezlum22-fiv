void timer0CTCInit(){
    TCCR0A = (1 << WGM01); // CTC mode
    TCCR0B = (1 << CS01);  // Prescaler = 8 
    OCR0A = 249;           // ist der wert wo der Zähler zurückgesetzt wird und der Interrupt ausgelöst wird 
    TIMSK0 |= (1 << OCIE0A); // Einschalten der Timer0 COMPA Interrupts
}